int		ft_power(int nb, int power)
{
	int cp;

	if (power == 0)
		return (1);
	cp = nb;
	while (power > 1)
	{
		nb = nb * cp;
		power--;
	}
	if (power < 0)
		return (0);
	return (nb);
}
