int		ft_nbrlen(int nb)
{
	int		i;

	if (nb > 2147483647 || nb < -2147483648)
		return (0);
	else
	{
		i = 0;
		while (nb != 0)
		{
			i++;
			nb /= 10;
		}
		return (i);
	}
}
